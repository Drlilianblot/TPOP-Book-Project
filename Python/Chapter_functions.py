def print_lyrics():
    print("I'm a lumberjack, and I'm okay.")
    print("I sleep all night and I work all day.")

def repeat_lyrics():
    print_lyrics()
    print_lyrics()

def print_twice(word):
    print(word)
    print(word)

def cat_twice(part1, part2):
    cat = part1 + part2
    print_twice(cat)
